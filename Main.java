class Main {
    public static void main(String[] a){
       // LOOP
       // loop - while, do while, for
       int i =1;
       while(i <= 4)
       {
            System.out.println("HI " + i);
            int j = 1;
            while (j <= 3) {
                
                System.out.println("Hello" + j);
                j++;
            }
            i++;
        }
        System.out.println("BYE"+ i);

    }
}